//! System info endpoints — `/api/system/...`
//!
//! These handlers return application metadata and open-source license information.
//! They are stubs that return placeholder data until the build pipeline produces
//! `backend-licenses.json` (via cargo-about) and `frontend-licenses.json`
//! (via license-checker).
//!
//! Any authenticated user may call these endpoints (doc 06 §About Page).

use axum::{
    body::Body,
    extract::State,
    http::{header, StatusCode},
    response::{IntoResponse, Response},
    Json,
};
use serde::Serialize;

use crate::state::AppState;

// ---------------------------------------------------------------------------
// Response types
// ---------------------------------------------------------------------------

#[derive(Serialize)]
pub struct AboutInfo {
    pub version: String,
    pub build: String,
    #[serde(rename = "serverHostname")]
    pub server_hostname: String,
    #[serde(rename = "eulaVersion")]
    pub eula_version: String,
}

#[derive(Serialize)]
pub struct LicenseEntry {
    pub name: String,
    pub version: String,
    pub license: String,
    pub copyright: String,
    pub text: String,
}

// ---------------------------------------------------------------------------
// Handlers
// ---------------------------------------------------------------------------

/// GET /api/system/about — returns application version, build info, and hostname.
pub async fn get_about(State(_state): State<AppState>) -> impl IntoResponse {
    let hostname = std::env::var("HOSTNAME")
        .or_else(|_| std::env::var("HOST"))
        .unwrap_or_else(|_| "unknown".into());

    let info = AboutInfo {
        version: env!("CARGO_PKG_VERSION").into(),
        build: option_env!("IO_BUILD_COMMIT")
            .unwrap_or("dev")
            .into(),
        server_hostname: hostname,
        eula_version: option_env!("IO_EULA_VERSION")
            .unwrap_or("1.0")
            .into(),
    };

    Json(serde_json::json!({ "success": true, "data": info }))
}

