/// GET /api/system/sbom — returns a CycloneDX JSON SBOM for download.
///
/// In production this file is generated at build time and served from disk.
/// This stub returns a minimal valid CycloneDX 1.4 document.
pub async fn download_sbom(State(_state): State<AppState>) -> impl IntoResponse {
    let sbom = serde_json::json!({
        "bomFormat": "CycloneDX",
        "specVersion": "1.4",
        "version": 1,
        "metadata": {
            "timestamp": chrono::Utc::now().to_rfc3339(),
            "component": {
                "type": "application",
                "name": "inside-operations",
                "version": env!("CARGO_PKG_VERSION")
            }
        },
        "components": []
    });

    let body = serde_json::to_string_pretty(&sbom).unwrap_or_default();

    Response::builder()
        .status(StatusCode::OK)
        .header(header::CONTENT_TYPE, "application/json")
        .header(
            header::CONTENT_DISPOSITION,
            "attachment; filename=\"io-sbom.json\"",
        )
        .body(Body::from(body))
        .unwrap_or_else(|_| StatusCode::INTERNAL_SERVER_ERROR.into_response())
}
